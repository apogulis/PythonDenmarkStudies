# Speed of the car in miles per hour
speed = 60

# Time car will travel in hours
time1 = 5
time2 = 8
time3 = 12

# Calculating the distance (Speed x Time)
distance1 = speed * time1
distance2 = speed * time2
distance3 = speed * time3

print("The car will travel", distance1, "miles in 5 hours.")
print("The car will travel", distance2, "miles in 8 hours.")
print("The car will travel", distance3, "miles in 12 hours.")
